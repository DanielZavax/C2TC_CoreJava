package c2ct.sample.pro1;

public class Char {

	public static void main(String[] args) {
		char ch = 'a';
		System.out.println(ch);
		char ch1 = 65;
		System.out.println(ch1);
		char var1 = '\u59A2';
		System.out.println(var1);
		char var2 = '\u30AC';
		System.out.println(var2);
		int a = 'A';
		System.out.println(a);

	}

}
