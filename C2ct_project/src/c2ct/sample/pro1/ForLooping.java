package c2ct.sample.pro1;

public class ForLooping {
		String name;
		int regNo;
		
		
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getRegNo() {
			return regNo;
		}
		public void setRegNo(int regNo) {
			this.regNo = regNo;
		}
		@Override
		public String toString() {
			return "ForLooping [name=" + name + ", regNo=" + regNo + "]";
		}
	 
	

}
